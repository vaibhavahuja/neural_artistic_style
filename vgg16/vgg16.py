import tensorflow as tf
import numpy as np

class vgg16:
    def __init__(self, img=None, train=False):
        self.train = train
        self.parameters = [] # used for loading pre-trained model
        self.inputlayer(img, self.train)
        self.convlayers()

    def inputlayer(self, img, train):
        self.img_height = img.shape[0]
        self.img_width = img.shape[1]
        img = np.asarray([img], dtype='float32')
        with tf.name_scope('input') as scope:
            self.img = tf.Variable(img, name='image',trainable=train)

    def convlayers(self):

        # zero-mean input
        # with tf.name_scope('preprocess') as scope:
        #     self.img = self.img - self.img_mean

        # conv1_1
        with tf.name_scope('conv1_1') as scope:
            kernel = tf.Variable(tf.truncated_normal([3, 3, 3, 64], dtype=tf.float32,
                                                     stddev=1e-1), name='weights', trainable=False)
            conv = tf.nn.conv2d(self.img, kernel, [1, 1, 1, 1], padding='SAME')
            biases = tf.Variable(tf.constant(0.0, shape=[64], dtype=tf.float32),
                                 trainable=False, name='biases')
            out = tf.nn.bias_add(conv, biases)
            self.conv1_1 = tf.nn.relu(out, name=scope)
            self.parameters += [kernel, biases]

